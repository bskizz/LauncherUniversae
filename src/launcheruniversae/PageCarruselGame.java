    private void asignarCarrusel1() {

        if (juegoMostrar == 1) {
            listaRutasCarrusel = Utilidades.createStringList("src/launcheruniversae/img/PageCarruselGame/", "Embarque", ".png", 5);
        } else if (juegoMostrar == 2) {
            listaRutasCarrusel = Utilidades.createStringList("src/launcheruniversae/img/PageCarruselGame/", "Hemorragia", ".png", 5);
        } else if (juegoMostrar == 3) {
            listaRutasCarrusel = Utilidades.createStringList("src/launcheruniversae/img/PageCarruselGame/", "Extincion", ".png", 5);
        } else if (juegoMostrar == 4) {
            listaRutasCarrusel = Utilidades.createStringList("src/launcheruniversae/img/PageCarruselGame/", "EPIS", ".png", 5);
        } else if (juegoMostrar == 5) {
            listaRutasCarrusel = Utilidades.createStringList("src/launcheruniversae/img/PageCarruselGame/", "Helicoptero", ".png", 5);
        } else if (juegoMostrar == 6) {
            listaRutasCarrusel = Utilidades.createStringList("src/launcheruniversae/img/PageCarruselGame/", "Ascensor", ".png", 5);
        }
    }


